package ex01_field;

public class User {

	// User 구성하는 속성(Attribute)이 필드(field)이다.
	// 변수(main안에 있는)라고 부르지 않고, 클래스 안에 있는 "필드"라고 부른다.
	// 필드는 일반 변수와 달리 자동으로 초기화된다.
	
	// 필드 5개			// 초기화
	String id;			// null
	String password;	// null
	String email;		// null
	int point;			// 0
	boolean isVip;		// false

}
